package Operators;

public class Relational {

	public static void main(String[] args) {

		int a = 20;
		int b = 30;

		System.out.println("a == b : "+(a==b));//a not equal to b....so returns false
		System.out.println("a != b : "+(a!=b));//a not equal to b....so returns true
		System.out.println("a < b : "+(a<b));//a less than b....so returns true
		System.out.println("a > b : "+(a>b));//a is not greater than b....so returns false
		System.out.println("a <= b : "+(a<=b));//a less than or equal to b....so returns true
		System.out.println("a >= b : "+(a>=b));//a is not greater than or equal to b....so returns false
	}

}
