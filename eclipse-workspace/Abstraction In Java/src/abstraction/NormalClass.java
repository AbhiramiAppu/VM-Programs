package abstraction;

public class NormalClass extends AbstractClass {
	
	public void abstractMethod1() {
		System.out.println("This is the First Abstract Method");
	}
	
	public void abstractMethod2() {
		System.out.println("This is the Second Abstract Method");
	}
	
	public void normalMethod3() {
		System.out.println("This is the Third Normal Method");
	}

}
